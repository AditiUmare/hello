
let num = 20

if (num > 0) {
    console.log("The number is positive.");
}

if (num % 2 === 0) {
    console.log("The number is even.");
} else {
    console.log("The number is odd.");
}

if (num > 0) {
    if (num > 100) {
        console.log("The number is large.");
    } else {
        console.log("The number is small.");
    }
} else {
    console.log("The number is negative or zero.");
}
