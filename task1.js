// a. Convert a string input to a number using Number()
let str = "63";  
let strToNumber = Number(str);
console.log("String to Number:", strToNumber); 

// b. Convert a number to a string using String()
let num = 100;
let numToString = String(num);
console.log("Number to String:", numToString);  

// c. Convert a string input to a boolean using Boolean()
let strBool = "true"; 
let strToBoolean = Boolean(strBool);
console.log("String to Boolean:", strToBoolean);  

// d. Convert a boolean to a number
let bool = true;
let boolToNumber = Number(bool);
console.log("Boolean to Number:", boolToNumber);  
